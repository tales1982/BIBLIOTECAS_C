#include "ft_printf.h"

int	print_string(char *s)
{
	unsigned int	i;

	i = 0;
	if (!s)
    {
        write(1, "(null)", 6);
        return 6;
    }
	while (s[i] != '\0')
    {
        i++;
    }
    write(1, s, i);
	return (i);
}
